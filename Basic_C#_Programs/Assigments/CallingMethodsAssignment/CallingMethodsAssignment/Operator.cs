using System;


namespace CallingMethodsAssignment
{
    public class Operator
    {
        public int Add(int data) // add method
        {
            return data + 4;
        }

        public int Subtract(int data) // subtract method
        {
            return data - 2;
        }

        public int Divide(int data) // divide method
        {
            return data / 2; 
        }
    }
}